def bot_token():
    from environ import DISCORD_BOT_TOKEN
    if not DISCORD_BOT_TOKEN:
        logger.critical("ğŸš« DISCORD_BOT_TOKEN is not set.")
        raise SystemExit(1)
    return DISCORD_BOT_TOKEN

